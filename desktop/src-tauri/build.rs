/*
 * 文件名: build.rs
 * 描述: Tauri 构建脚本入口，用于生成桌面应用所需的构建信息。
 * 作用: 在构建阶段注入 Tauri 配置，并补全缺失的图标资源。
 */

// 内置 1x1 PNG 图标，用于补全缺失的桌面图标文件
const ICON_PNG: &[u8] = &[
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, 0x00, 0x00, 0x00, 0x0d, 0x49, 0x48,
    0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x08, 0x06, 0x00, 0x00,
    0x00, 0x1f, 0x15, 0xc4, 0x89, 0x00, 0x00, 0x00, 0x0b, 0x49, 0x44, 0x41, 0x54, 0x78,
    0x9c, 0x63, 0x60, 0x00, 0x02, 0x00, 0x00, 0x05, 0x00, 0x01, 0x7a, 0x5e, 0xab, 0x3f,
    0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82,
];

fn main() {
    // 确保默认图标存在，避免 Tauri 构建阶段因缺失图标失败
    let manifest_dir = std::env::var("CARGO_MANIFEST_DIR").expect("manifest dir");
    let icons_dir = std::path::Path::new(&manifest_dir).join("icons");
    let icon_path = icons_dir.join("icon.png");
    std::fs::create_dir_all(&icons_dir).expect("create icons dir");

    // 直接写入 RGBA 图标，避免旧图标格式不匹配导致构建失败
    std::fs::write(&icon_path, ICON_PNG).expect("write icon");

    // 触发 Tauri 构建流程，生成应用元信息与资源映射
    tauri_build::build()
}
